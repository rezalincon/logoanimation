


    function myFunction() {
        
        var element = document.getElementById("exampleModal");
        element.classList.add("animate__backOutDown");
        $('.modal-backdrop').remove();
        
        setTimeout(function () {
            $('#exampleModal').modal('hide');
            $('#exampleModal').removeClass('animate__backOutDown');
        }, 600);
    }



